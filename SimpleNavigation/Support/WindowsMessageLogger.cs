using System;
using System.Diagnostics;
using System.Runtime.InteropServices;

namespace SimpleNavigation;

public class MessageEventArgs : EventArgs
{
    public string Code { get; set; } = string.Empty;
    public string Value { get; set; } = string.Empty;
    public DateTime Time { get; set; } = DateTime.MinValue;
}

/// <summary>
/// https://learn.microsoft.com/en-us/windows/win32/winmsg/window-notifications
/// </summary>
public class WindowsMessageLogger
{
    public event EventHandler<MessageEventArgs>? WndProcMsgReceived;
    delegate IntPtr WndProc(IntPtr hWnd, uint msg, IntPtr wParam, IntPtr lParam);
    const int GWLP_WNDPROC = -4;
    IntPtr _oldWndProc;
    WndProc _newWndProc;

    public void StartLogging(IntPtr hWnd)
    {
        _newWndProc = new WndProc(MyWndProc);
        _oldWndProc = SetWindowLongPtr(hWnd, GWLP_WNDPROC, _newWndProc);
    }

    /// <summary>
    /// Delegate for WndProc handler.
    /// </summary>
    IntPtr MyWndProc(IntPtr hWnd, uint msg, IntPtr wParam, IntPtr lParam)
    {
        try
        {
            //Debug.WriteLine($"⇨ {(WinMsg)msg}");
            // Fire the event.
            OnMessageReceived(new MessageEventArgs { Code = $"{(WinMsg)msg}", Value = $"0x{msg:X4}", Time = DateTime.Now }); ;

            // Call the original window procedure
            return CallWindowProc(_oldWndProc, hWnd, msg, wParam, lParam);
        }
        catch (Exception)
        { 
            return IntPtr.Zero; 
        }
    }

    protected virtual void OnMessageReceived(MessageEventArgs e) => WndProcMsgReceived?.Invoke(this, e);

    [DllImport("user32.dll", CharSet = CharSet.Auto)]
    private static extern IntPtr SetWindowLongPtr(IntPtr hWnd, int nIndex, WndProc dwNewLong);

    [DllImport("user32.dll", CharSet = CharSet.Auto)]
    private static extern IntPtr CallWindowProc(IntPtr lpPrevWndFunc, IntPtr hWnd, uint Msg, IntPtr wParam, IntPtr lParam);
}

